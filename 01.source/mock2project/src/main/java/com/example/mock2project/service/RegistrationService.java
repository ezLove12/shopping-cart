    private final EmailValidator emailValidator;
    private final TokenService tokenService;
    private final EmailSender emailSender;


    public RegistrationService(UserService userService, EmailValidator emailValidator, TokenService tokenService, EmailSender emailSender) {
        this.userService = userService;
        this.emailValidator = emailValidator;
        this.tokenService = tokenService;
        this.emailSender = emailSender;
    }

    public String register(User user){
        boolean isValid = emailValidator.test(user.getEmail());
        if(isValid){
            String token = userService.signUpUser(user);
            String link = "http://localhost:8080/confirm?token=" + token;
            emailSender.sendEmail(user.getEmail(), buildEmail(user.getUsername(), link));
            return token;
        }else{
            throw new IllegalStateException(String.format("Email %s, not valid", user.getEmail()));
        }
    }

    public String confirmToken(String token){
        Optional<SignInToken> confirmToken = tokenService.getToken(token);

        if(!confirmToken.isPresent()){
            throw new IllegalStateException("Token not found");
        }

        if(confirmToken.get().getConfirmedAt()!=null){
            throw new IllegalStateException("Email is already confirmed");
        }

        LocalDateTime expiresAt = confirmToken.get().getExpiresAt();
        if(expiresAt.isBefore(LocalDateTime.now())){
            throw new IllegalStateException("Token is already expired!");
        }

        tokenService.setConfirmedAt(token);
        userService.enableUser(confirmToken.get().getAppUser().getEmail());
        userService.changeRole(confirmToken.get().getAppUser().getId());
        return "Your email is confirmed. Thank you for using our service!";
    }

